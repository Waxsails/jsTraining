//  Arithmetic
console.log(5 + 3); // 8

//  Comparison
console.log(5 > 3); // true

//  Logical
console.log(true && false); // false

//  Ternary
const age2 = 18;
console.log(age2 >= 18 ? "Adult" : "Minor"); // Adult

//  Nullish coalescing
let value = null ?? "default";
console.log(value); // "default"